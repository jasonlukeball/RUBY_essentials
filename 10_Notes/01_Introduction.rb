# Objects
# Objects are instances of classes

=begin

--------------
 OBJECTS
--------------

OBJECTS are things
(eg a Person)

OBJECTS have attributes
(eg name, age, gender)


--------------
 METHODS
--------------

METHODS are things an OBJECT can do
(eg a person can walk, drive, code)

METHODS are procedures associated with a CLASS


--------------
 CLASSES
--------------

CLASSES are blueprints of OBJECTS
(you need to have a class before you can create an object)


=end


